    private String printarChar() {
        int i, j;
        String sla = "";
        for (i = 0; i < tam; i++) {
            for (j = 0; j < tam; j++) {
                sla += String.valueOf(tabuleiro[i][j]) + "   ";
            }
            sla += "\n";
        }
        return sla;
    }

