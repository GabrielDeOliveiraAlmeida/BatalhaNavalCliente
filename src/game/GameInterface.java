    public void atirar() {
        int x, y;
        try {
            x = Integer.parseInt(x1.getText());
            y = Integer.parseInt(y1.getText());
            if (x >= tam || y >= tam) {
                JOptionPane.showMessageDialog(this, "Insira Numeros Validos", "ATENÇÃO", HEIGHT);
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Insira Numeros Validos", "ATENÇÃO", HEIGHT);
            return;
        }
        if (tabuleiro[x][y] == 'O' || tabuleiro[x][y] == 'X') {
            alerta.setText("Campo já atingido");
        } else {
            vez.setText("FOGO");
            control.fogo(x, y);
        }
    }

